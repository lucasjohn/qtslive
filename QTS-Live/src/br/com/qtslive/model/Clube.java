package br.com.qtslive.model;

/**
 * 
 * Classe que representa um clube.
 * 
 * @author Tomás Azevedo
 *
 */
public class Clube {
	
	private Long id;
	private String nome;
	
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	
	

}
